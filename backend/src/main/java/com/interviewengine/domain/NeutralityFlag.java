package com.interviewengine.domain;

/**
 * Замечание к работе интервьюера, выявленное по тексту его реплик: наводящий
 * вопрос, подсказка ответа, требование согласия и т.п.
 *
 * @param kind     тип проблемы (см. {@link Kind})
 * @param blockId  id блока, в котором замечено
 * @param trigger  фрагмент-маркер из словаря, который сработал
 * @param quote    дословная цитата реплики интервьюера
 */
public record NeutralityFlag(
        Kind kind,
        String blockId,
        String trigger,
        String quote
) {

    /**
     * Типы наблюдений по нейтральности интервьюера.
     */
    public enum Kind {
        /**
         * Подсказывает согласие: «верно?», «не так ли?», «правильно?».
         */
        SUGGESTIVE_AGREEMENT,
        /**
         * Перефразирует ответ кандидата в правильную сторону: «то есть вы имели в виду…».
         */
        ANSWER_REFRAMING,
        /**
         * Прямой намёк на ответ: «обычно делают через каналы», «вы наверное использовали мьютекс?».
         */
        ANSWER_HINT
    }
}
